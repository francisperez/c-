/*Fichero que calcula
 la suma de dos números*/
#include <iostream>
using namespace std;
int main () {
	int a, b, res;
	cout << "Introduce el primer número: ";
	cin >> a; //Este es el primer número
	cout << "Introduce el segundo número: ";
	cin >> b; //Y este es el segundo número
	res = a + b;
	cout << "El resultado es: "<< res << endl;
}
